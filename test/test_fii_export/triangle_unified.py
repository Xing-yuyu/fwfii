"""
三机三角编队 — 统一飞行脚本 (专用于测试 FiiExporter)
======================================================
560cm (6m) 定位毯, 3 架 F600 无人机, 多层分离飞行。

安全约束:
  - Z 全部控制在 100~250cm 范围内
  - 同时刻任意两机 XY 投影间距 > 60cm (同层不重叠)
  - 三机走不同边线交换位置，路径不交叉

无人机 (分散站位):
  - UAVID=98101  起始 (100, 100)  低空层 110~140cm
  - UAVID=98102  起始 (460, 100)  中空层 150~180cm
  - UAVID=98103  起始 (280, 460)  高空层 190~240cm

航线:
  Phase 1 (0~3s):    解锁 + 三色闪烁
  Phase 2 (3~6s):    分层起飞 (Z=120/170/220)
  Phase 3 (6~12s):   三角展开 → 毯三角顶点 + 青色
  Phase 4 (12~19s):  顺时针换边 (各沿毯边线移动，路径不交叉) + 紫色
  Phase 5 (19~24s):  中心安全汇合 (同XY不同Z) + 黄灯同步
  Phase 6 (24~30s):  回起始位 + 降落 + 金色告别
"""
from fwfii import Flight, Arm, Disarm, Takeoff, Land, Move2, Delay
from fwfii import MaxVelXY, MaxVelZ, MaxAccXY, MaxAccZ
from fwfii import AllOn, AllOff, AllBlink, AllBreath
from fwfii import RED, GREEN, BLUE, YELLOW, CYAN, PURPLE, WHITE, ORANGE

# ── 三架无人机 ──
f1 = Flight(98101)  # 低空层  — 左前角 (100, 100)
f2 = Flight(98102)  # 中空层  — 右前角 (460, 100)
f3 = Flight(98103)  # 高空层  — 后方中心 (280, 460)

# ── 安全参数 ──
for f in (f1, f2, f3):
    MaxVelXY(f, 250)
    MaxVelZ(f, 120)
    MaxAccXY(f, 400)
    MaxAccZ(f, 350)

# ═══════════════════════════════════════════════
# Phase 1: 解锁 + 三色闪烁 (0s ~ 3s)
# ═══════════════════════════════════════════════
AllBlink(f1, RED,   300, 300, ts=0)
AllBlink(f2, GREEN, 300, 300, ts=0)
AllBlink(f3, BLUE,  300, 300, ts=0)
Arm(f1, ts=500)
Arm(f2, ts=500)
Arm(f3, ts=500)

Delay(2500)

# ═══════════════════════════════════════════════
# Phase 2: 分层起飞 (3s ~ 6s)
#   f1→120  f2→170  f3→220  全部在 100~250 内
#   XY 保持起始点，三机间距 > 60cm ✓
# ═══════════════════════════════════════════════
AllOn(f1, GREEN)
AllOn(f2, GREEN)
AllOn(f3, GREEN)
Takeoff(f1, 120)
Takeoff(f2, 170)
Takeoff(f3, 220)
Delay(5000)

# ═══════════════════════════════════════════════
# Phase 3: 三角展开 (6s ~ 12s)
#   f1→( 80,  80, 140)  左前顶点
#   f2→(480,  80, 180)  右前顶点
#   f3→(280, 480, 240)  后方顶点
#   三机 XY 距离均 > 200cm ✓
# ═══════════════════════════════════════════════
AllOn(f1, CYAN)
AllOn(f2, CYAN)
AllOn(f3, CYAN)
Move2(f1,  80,  80, 140)
Move2(f2, 480,  80, 180)
Move2(f3, 280, 480, 240)
Delay(6000)

# ═══════════════════════════════════════════════
# Phase 4: 顺时针换边 (12s ~ 19s)
#   各沿毯边线移动，路径完全不交叉:
#     f1 沿底边 y=80:   (80,80)→(480,80)     z=130
#     f2 沿右边 x=480:  (480,80)→(480,480)    z=170
#     f3 沿顶边 y=480:  (280,480)→(80,480)    z=230
#   Z 差: f1/f2=40, f2/f3=60, f1/f3=100 ✓
# ═══════════════════════════════════════════════
AllOn(f1, PURPLE)
AllOn(f2, PURPLE)
AllOn(f3, PURPLE)
Move2(f1, 480,  80, 130)   # 底边右移
Move2(f2, 480, 480, 170)   # 右边上移
Move2(f3,  80, 480, 230)   # 顶边左移
Delay(7000)

# ═══════════════════════════════════════════════
# Phase 5: 中心安全汇合 (19s ~ 24s)
#   三机 XY 均移到 (280, 280)，但 Z 不同层:
#     f1→(280, 280, 130)  低层
#     f2→(280, 280, 170)  中层  Z差=40
#     f3→(280, 280, 230)  高层  Z差=60
#   XY 重叠但 Z 分层，安全 ✓
# ═══════════════════════════════════════════════
AllOn(f1, YELLOW)
AllOn(f2, YELLOW)
AllOn(f3, YELLOW)
Move2(f1, 280, 280, 130)
Move2(f2, 280, 280, 170)
Move2(f3, 280, 280, 230)
Delay(3000)

# 同步黄灯闪烁 (空中定点灯光秀)
AllBlink(f1, YELLOW, 200, 200)
AllBlink(f2, YELLOW, 200, 200)
AllBlink(f3, YELLOW, 200, 200)
Delay(2000)

# ═══════════════════════════════════════════════
# Phase 6: 回起始位 + 降落 (24s ~ 30s)
#   中心分散回各自起点:
#     f1→(100, 100, 110)
#     f2→(460, 100, 160)
#     f3→(280, 460, 210)
#   三机 XY 再次拉开 > 200cm ✓
# ═══════════════════════════════════════════════
AllOn(f1, GREEN)
AllOn(f2, GREEN)
AllOn(f3, GREEN)
Move2(f1, 100, 100, 110)
Move2(f2, 460, 100, 160)
Move2(f3, 280, 460, 210)
Delay(3000)

# 降落 + 金色告别闪烁
AllBlink(f1, YELLOW, 300, 300)
AllBlink(f2, YELLOW, 300, 300)
AllBlink(f3, YELLOW, 300, 300)
Land(f1, ts=29000)
Land(f2, ts=29000)
Land(f3, ts=29000)
Delay(3000)

# 上锁 + 熄灯
AllOff(f1, ts=32500)
AllOff(f2, ts=32500)
AllOff(f3, ts=32500)
Disarm(f1, ts=33000)
Disarm(f2, ts=33000)
Disarm(f3, ts=33000)
